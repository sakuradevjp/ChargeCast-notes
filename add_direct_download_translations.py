missing = []
for lang, section in data.items():
    if lang not in T:
        missing.append(lang)
        continue
    new = OrderedDict(section)
    for key, value in T[lang].items():
        new[key] = value
    data[lang] = new

if missing:
    print(f"WARNING: no direct-download translations for: {missing}")

LANGS_PATH.write_text(
    json.dumps(data, ensure_ascii=False, indent=2) + "\n",
    encoding="utf-8",
)
